    private void tabela_vendasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabela_vendasMouseClicked
        try {

            FRM_Detalhes dts = new FRM_Detalhes();
            dts.txtnome.setText(tabela_vendas.getValueAt(tabela_vendas.getSelectedRow(), 2).toString());
            dts.txttotalvenda.setText(tabela_vendas.getValueAt(tabela_vendas.getSelectedRow(), 3).toString());
            dts.txtdata.setText(tabela_vendas.getValueAt(tabela_vendas.getSelectedRow(), 1).toString());
            dts.txtobs.setText(tabela_vendas.getValueAt(tabela_vendas.getSelectedRow(), 4).toString());

            int id = Integer.parseInt(tabela_vendas.getValueAt(tabela_vendas.getSelectedRow(), 0).toString());
            Itens_Vendas ivd = new Itens_Vendas();
            Item_vendadao daov = new Item_vendadao();

            List<Itens_Vendas> lista = daov.listaritens(id);

            DefaultTableModel dados = (DefaultTableModel) dts.tabela_Itens_vendidos.getModel();

            dados.setNumRows(0);

            for (Itens_Vendas i : lista) {
                dados.addRow(new Object[]{
                    i.getPdt().getNome_produto(),
                    i.getQtd(),
                    i.getPdt().getPreco(),
                    i.getSubtotal()

                });
            }

            dts.setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tabela_vendasMouseClicked

