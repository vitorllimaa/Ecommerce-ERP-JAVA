    private void btdataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btdataActionPerformed
        try {
            DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate data_inicio = LocalDate.parse(txtdatai.getText(), formato);
            LocalDate data_final = LocalDate.parse(txtdataf.getText(), formato);

            Vendadao dao = new Vendadao();
            List<Vendas> lista = dao.listarvendas(data_inicio, data_final);

            DefaultTableModel dados = (DefaultTableModel) tabela_vendas.getModel();
            dados.setNumRows(0);

            for (Vendas c : lista) {
                dados.addRow(new Object[]{
                    c.getId(),
                    c.getData_venda(),
                    c.getCliente_id().getNome(),
                    c.getTotal_venda(),
                    c.getObs()
                });

            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Data não encontrada!");
        }

    }//GEN-LAST:event_btdataActionPerformed

